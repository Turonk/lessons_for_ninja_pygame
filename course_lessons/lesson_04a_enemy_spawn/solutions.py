import random

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è —Ä–∞—Å—á–µ—Ç–∞ —É—Ä–æ–Ω–∞
def calculate_damage(attack_power, weapon_type):
    """
    –†–∞—Å—Å—á–∏—Ç—ã–≤–∞–µ—Ç —É—Ä–æ–Ω –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç —Å–∏–ª—ã –∞—Ç–∞–∫–∏ –∏ —Ç–∏–ø–∞ –æ—Ä—É–∂–∏—è
    """
    if weapon_type == "–º–µ—á":
        multiplier = 2
    elif weapon_type == "–∫–æ–≥—Ç–∏":
        multiplier = 1.5
    elif weapon_type == "–∫—É–ª–∞–∫–∏":
        multiplier = 1
    else:
        multiplier = 0.5  # –î–ª—è –Ω–µ–∏–∑–≤–µ—Å—Ç–Ω–æ–≥–æ –æ—Ä—É–∂–∏—è

    return int(attack_power * multiplier)

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –∂–∏–≤ –ª–∏ –ø–µ—Ä—Å–æ–Ω–∞–∂
def is_alive(health):
    """
    –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç True –µ—Å–ª–∏ –∑–¥–æ—Ä–æ–≤—å–µ > 0, –∏–Ω–∞—á–µ False
    """
    return health > 0

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ —Å–∫–æ—Ä–æ—Å—Ç–∏ –≤—Ä–∞–≥–∞
def generate_enemy_speed(enemy_type):
    """
    –ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç —Å–ª—É—á–∞–π–Ω—É—é —Å–∫–æ—Ä–æ—Å—Ç—å –¥–ª—è –≤—Ä–∞–≥–∞ –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç –µ–≥–æ —Ç–∏–ø–∞
    """
    if enemy_type == "–≤–æ–ª–∫":
        return random.randint(7, 12)
    elif enemy_type == "–ª–∏—Å–∞":
        return random.randint(5, 10)
    elif enemy_type == "—Å–æ–±–∞–∫–∞":
        return random.randint(6, 11)
    elif enemy_type == "–∫—Ä—ã—Å–∞":
        return random.randint(3, 8)
    elif enemy_type == "–∑–º–µ—è":
        return random.randint(4, 9)
    else:
        return random.randint(1, 15)  # –î–ª—è –Ω–µ–∏–∑–≤–µ—Å—Ç–Ω–æ–≥–æ —Ç–∏–ø–∞

# –û—Å–Ω–æ–≤–Ω–∞—è –ø—Ä–æ–≥—Ä–∞–º–º–∞ - –º–∏–Ω–∏-–∏–≥—Ä–∞
def battle_simulation():
    print("üê± –ù–∞—á–∏–Ω–∞–µ—Ç—Å—è –±–æ–π –∫–æ—Ç–∞-–Ω–∏–Ω–¥–∑—è! ‚öîÔ∏è")

    # –°–æ–∑–¥–∞–µ–º –∫–æ—Ç–∞
    cat_health = 100
    cat_attack = 15
    cat_weapon = "–º–µ—á"

    # –°–æ–∑–¥–∞–µ–º –≤—Ä–∞–≥–∞
    enemy_types = ["–≤–æ–ª–∫", "–ª–∏—Å–∞", "—Å–æ–±–∞–∫–∞", "–∫—Ä—ã—Å–∞", "–∑–º–µ—è"]
    enemy_type = random.choice(enemy_types)
    enemy_health = 80
    enemy_speed = generate_enemy_speed(enemy_type)

    print(f"–ö–æ—Ç-–Ω–∏–Ω–¥–∑—è vs {enemy_type.capitalize()} (—Å–∫–æ—Ä–æ—Å—Ç—å: {enemy_speed})")
    print(f"–ó–¥–æ—Ä–æ–≤—å–µ –∫–æ—Ç–∞: {cat_health}")
    print(f"–ó–¥–æ—Ä–æ–≤—å–µ {enemy_type}–∞: {enemy_health}")
    print()

    # –ë–æ–π –ø—Ä–æ–¥–æ–ª–∂–∞–µ—Ç—Å—è –ø–æ–∫–∞ –æ–±–∞ –∂–∏–≤—ã
    round_num = 1
    while is_alive(cat_health) and is_alive(enemy_health):
        print(f"--- –†–∞—É–Ω–¥ {round_num} ---")

        # –ö–æ—Ç –∞—Ç–∞–∫—É–µ—Ç
        cat_damage = calculate_damage(cat_attack, cat_weapon)
        enemy_health -= cat_damage
        print(f"–ö–æ—Ç –∞—Ç–∞–∫—É–µ—Ç {cat_weapon}–æ–º –∏ –Ω–∞–Ω–æ—Å–∏—Ç {cat_damage} —É—Ä–æ–Ω–∞!")

        if not is_alive(enemy_health):
            print(f"{enemy_type.capitalize()} –ø–æ–≤–µ—Ä–∂–µ–Ω! üéâ")
            break

        # –í—Ä–∞–≥ –∞—Ç–∞–∫—É–µ—Ç (–ø—Ä–æ—Å—Ç–∞—è –∞—Ç–∞–∫–∞)
        enemy_damage = random.randint(5, 15)
        cat_health -= enemy_damage
        print(f"{enemy_type.capitalize()} –∞—Ç–∞–∫—É–µ—Ç –∏ –Ω–∞–Ω–æ—Å–∏—Ç {enemy_damage} —É—Ä–æ–Ω–∞!")

        if not is_alive(cat_health):
            print("–ö–æ—Ç –ø–æ–≤–µ—Ä–∂–µ–Ω! üíî")
            break

        print(f"–ó–¥–æ—Ä–æ–≤—å–µ –∫–æ—Ç–∞: {cat_health}")
        print(f"–ó–¥–æ—Ä–æ–≤—å–µ {enemy_type}–∞: {enemy_health}")
        print()
        round_num += 1

    # –ò—Ç–æ–≥ –±–æ—è
    if cat_health > enemy_health:
        print("üèÜ –ö–æ—Ç-–Ω–∏–Ω–¥–∑—è –ø–æ–±–µ–¥–∏–ª!")
    else:
        print("üò¢ –í—Ä–∞–≥ –ø–æ–±–µ–¥–∏–ª!")

# –ó–∞–ø—É—Å–∫–∞–µ–º —Å–∏–º—É–ª—è—Ü–∏—é –±–æ—è
battle_simulation()

print("\n" + "="*50 + "\n")

