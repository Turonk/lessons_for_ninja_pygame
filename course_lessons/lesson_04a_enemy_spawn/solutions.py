# –¢–µ—Å—Ç is_alive
print(f"–ö–æ—Ç —Å –∑–¥–æ—Ä–æ–≤—å–µ–º 50 –∂–∏–≤: {is_alive(50)}")
print(f"–ö–æ—Ç —Å –∑–¥–æ—Ä–æ–≤—å–µ–º 0 –∂–∏–≤: {is_alive(0)}")
print(f"–ö–æ—Ç —Å –∑–¥–æ—Ä–æ–≤—å–µ–º -10 –∂–∏–≤: {is_alive(-10)}")

# –¢–µ—Å—Ç generate_enemy_speed (–Ω–µ—Å–∫–æ–ª—å–∫–æ —Ä–∞–∑ –¥–ª—è –∫–∞–∂–¥–æ–≥–æ —Ç–∏–ø–∞)
